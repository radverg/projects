          <h2> About Tank hunt: </h2>
        </header>
        <section>
           <p>This website is about my game called "Tank hunt" developed using C# programming language with XNA framework and Lidgren-Network library.
           It is a 2D game with very simple graphics where players fight each other using their tanks in randomly generated labyrinth battlefield.
           Players collect various weapons during the game and use them against their enemies, invisibility is also very funny element of the game. To learn more 
           go to the <a href="manual">manual section</a>.
           This game is multiplayer-only and it's really fun to play with your friends, during a skype call.
           However, I am not a professional programer - the game contains a lot of bugs which I couldn't fix out yet, due to lack of time, strength or skill.
           These bugs don't make the game unplayable though.</p>
           <p>Facebook page of the game: <a href="http://www.facebook.com/tankhuntgame">http://www.facebook.com/tankhuntgame</a></p>
