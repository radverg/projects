          <h2> Requirements for installing and running Tank hunt: </h2>
        </header>
        <section>
           <li>Device with Microsoft Windows 7 or higher (may work on XP also, but I haven't tested it).</li>
           <li>Installed Microsoft .NET framework 4.0 (it is probably pre-installed on your Windows) -
           <a href="http://www.microsoft.com/en-us/download/details.aspx?id=17718" target="_blank"> http://www.microsoft.com/en-us/download/details.aspx?id=17718</a> </li>
           <li>Installed XNA framework 4.0 redistributable (if you haven't done it yet, it isn't installed on Windows - dunno why)
            - <a href="http://www.microsoft.com/en-us/download/details.aspx?id=20914" target="_blank">http://www.microsoft.com/en-us/download/details.aspx?id=20914 </a> </li>
