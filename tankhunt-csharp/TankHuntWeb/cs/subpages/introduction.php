          <h2> O hře Tank hunt: </h2>
        </header>
        <section>
           <p>Toto je stránka o mé hře zvané "Tank hunt" naprogramované v programovacím jazyce C# s použitím knihoven XNA framework a Lidgren-Network library.
           Tank hunt je 2D hra s velice jednoduchou grafikou. Každý hráč má svůj tank a bojuje s ním proti ostatním hráčům v bludišti.
           Hráči sbírají různé zbraně během hry a používají je proti ostatním hráčům. Také je zde možnost použít neviditelnost, která dělá hru ještě zábavnější. 
           Pro více informací běžte do sekce <a href="index.php?page=manual">návody</a>.
           Hra je pouze multiplayerová, jelikož singleplayer by nebyl zábavný. Při hraní je doporučeno být s ostatními hráči na skypu nebo teamspeaku, protože to hru dělá ještě zábavnější.
           Nejsem však profesionální programátor, a tudíž hra obsahuje mnoho chyb, které zatím nemohu opravit z důvodu nedostatku času a znalostí.
           Tyto chyby však nezpůsobují nehratelnost hry.</p>
           <p>Facebooková stránka hry: <a href="http://www.facebook.com/tankhuntgame">http://www.facebook.com/tankhuntgame</a></p>
