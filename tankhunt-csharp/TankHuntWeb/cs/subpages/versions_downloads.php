          <h2> Verze 1.7 - informace: (29.10.2015): </h2>
        </header>
        <section>
            <li>Přidána nová zbraň: double mine (dvojitá mina) - můžete položit 2 miny na různá místa.</li> 
            <li>Hráči již neumírají, pokud vystřelí pod úhlem 135°.</li>
            <li>Hráči mohou zapnout/vypnout automatické otáčení při nárazu do stěny.</li>
            <li>Opravena chyba způsobující pomalou rychlost laserů.</li>
            <li>Hráčovi se odečte jeden kill, pokud zabije sám sebe. (proti zneužívání sebevražd)</li>
            <li>Hráči se již správně řadí na panelu vlevo.</li>
            <li>Mrtví hráči již nemohou sbírat zbraně vygenerované na místě jejich smrti.</li>
            <li>Odrážecí laser se již neodráží špatně od rohů zdí.</li>
            <li>Mrtví hráči jsou označeni na panelu vlevo.</li>
            <li>Novinka: Pozorovací mód: V pozorovacím módu hráč vidí ostatní viditelné i neviditelné hráče a zahrabané miny, může posouvat s kamerou a také vidět 
            vše v tmavých kolech. Pozorovatel se může na začátku kteréhokoli kola připojit do hry nebo může dále pozorovat ostatní, jak hrají. Každý hrající hráč 
            se může přepnout do role pozorovatele po své smrti kliknutím na tlačítko P. Pozorovací mód je dobrý na odpočinek.</li>
            <li>Hráčův tank již není vidět při odpočtu nového kola, i když před koncem minulého kola použil neviditelnost, která mu při odpočtu skončila.</li>
